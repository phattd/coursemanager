    private void  delete() {
        if (JOptionPane.showConfirmDialog(null, "Bạn có thật sự muốn xóa! Nhấn Yes để xóa"  ,
                "Warning", JOptionPane.YES_NO_OPTION) == JOptionPane.OK_OPTION) {
            ArrayList<String> arrayList = getField();
            String id = arrayList.get(0);
            String kh = arrayList.get(1);
            String ph = arrayList.get(2);
            Message m = lichHocBLL.remove(id, kh, ph);
            JOptionPane.showMessageDialog(null, m.toString());
            loadDanhSachLichHoc();
        }
    }
    private void search() {
        String qry = txtTimKiem.getText();
        ArrayList<LichHoc> searchList = lichHocBLL.search(qry);
        danhSachLichHoc=searchList;
        renderTimeLineTable= (DefaultTableModel) tbl_lichhoc.getModel();
        renderTimeLineTable.setNumRows(0);
        for (LichHoc index: danhSachLichHoc)
        {
            renderTimeLineTable.addRow(index.parseArray());
        }
        tbl_lichhoc.setModel(renderTimeLineTable);

     }


