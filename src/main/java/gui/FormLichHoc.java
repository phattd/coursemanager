    public void refreshField() {
        txtTimKiem.setText("");
        cmbCaHoc.setSelectedIndex(0);
        cmbPhong.setSelectedIndex(0);
        cmbThu.setSelectedIndex(0);
        cmbGV.setSelectedIndex(0);
       
    }
    public ArrayList<String> getField() {
        ArrayList<String> tmp = new ArrayList();
        tmp.add(giangVienLibReverse.get(cmbGV.getSelectedItem()));
        tmp.add(txtMaKH.getText());
        tmp.add(phongHocLibReverse.get(cmbPhong.getSelectedItem()));
        tmp.add(String.valueOf(cmbThu.getSelectedItem()));
        tmp.add(String.valueOf(caHocLibReverse.get(cmbCaHoc.getSelectedItem())));
        return tmp;
    }
    public void add () {
        ArrayList<String> data = getField();
        Message m = lichHocBLL.add(data);
        if (m == Message.OK) {
            JOptionPane.showMessageDialog(null,Message.OK.toString());
            loadDanhSachLichHoc();
        } else {
            JOptionPane.showMessageDialog(null, m.toString());
            refreshField();
        }
    }
    public void update () {

        ArrayList<String> data = getField();
        Message m = lichHocBLL.update(data);
        if (m == Message.OK) {
            JOptionPane.showMessageDialog(null,Message.OK.toString());
            loadDanhSachLichHoc();
        } else {
            JOptionPane.showMessageDialog(null, m.toString());
            refreshField();
        }
    }
    private void loadDanhSachKhoaHoc()
    {
        refreshField();
        ArrayList<KhoaHoc> danhSachKhoaHoc=khoaHocBLL.getListNotOpening();
        renderCourseTable= (DefaultTableModel) tbl_khoahoc.getModel();
        renderCourseTable.setNumRows(0);
        for (KhoaHoc index: danhSachKhoaHoc)
        {
            renderCourseTable.addRow(index.parseArrayLite());
        }
        tbl_khoahoc.setModel(renderCourseTable);
    }
    private void loadDanhSachLichHoc()
    {
        refreshField();
        danhSachLichHoc=lichHocBLL.getList();
        renderTimeLineTable= (DefaultTableModel) tbl_lichhoc.getModel();
        renderTimeLineTable.setNumRows(0);
        for (LichHoc index: danhSachLichHoc)
        {
            renderTimeLineTable.addRow(index.parseArray());
        }
        tbl_lichhoc.setModel(renderTimeLineTable);
    }
    private void loadDanhSachGiangVien(){
        giangVienLib.clear();
        giangVienLibReverse.clear();
        ArrayList<String> gvList = new ArrayList<>();
        ArrayList<GiangVien> giangViens = giangVienBLL.getList();
        for (GiangVien index : giangViens) {
            giangVienLib.put(index.getIdGiangVien(), index.getHoGV()+" "+index.getTenGV()+ " - Mã: "+index.getIdGiangVien().substring(0,10)+"..." );
            giangVienLibReverse.put(index.getHoGV()+" "+index.getTenGV()+ " - Mã: "+index.getIdGiangVien().substring(0,10)+"...", index.getIdGiangVien());
            gvList.add(index.getHoGV()+" "+index.getTenGV()+ " - Mã: "+index.getIdGiangVien().substring(0,10)+"...");
        }
        cmbGV.setModel(new DefaultComboBoxModel(gvList.toArray()));

    }
    private void loadDanhSachPhong(){
        phongHocLib.clear();
        phongHocLibReverse.clear();
        ArrayList<String> phList = new ArrayList<>();
        ArrayList<PhongHoc> phongHocs = phongHocBLL.getList();
        for (PhongHoc index : phongHocs) {
            phongHocLib.put(index.getIdPhongHoc(), index.getTenPhongHoc()+" - Mã: "+index.getIdPhongHoc().substring(0,10)+"...");
            phongHocLibReverse.put( index.getTenPhongHoc()+" - Mã: "+index.getIdPhongHoc().substring(0,10)+"...", index.getIdPhongHoc());

            phList.add(index.getTenPhongHoc()+" - Mã: "+index.getIdPhongHoc().substring(0,10)+"...");
        }
        cmbPhong.setModel(new DefaultComboBoxModel(phList.toArray()));

    }
    private void chooseRow(int row)
    {

        tbl_khoahoc.clearSelection();
        Vector temp=new Vector();
        for(int index=0;index<tbl_lichhoc.getColumnCount();index++)
        {
            temp.add(tbl_lichhoc.getValueAt(row,index));
        }
        txtMaKH.setText(""+temp.get(0));
        cmbGV.setSelectedItem(giangVienLib.get(temp.get(1)));
        cmbPhong.setSelectedItem(phongHocLib.get(temp.get(2)));
        cmbThu.setSelectedItem(temp.get(3));
        cmbCaHoc.setSelectedItem(caHocLib.get(temp.get(4)));


    }

